export function getLogsByTimescale(timescale: string): LogEntry[] {
  return LOG_ENTRIES
    .filter(entry => entry.timescale === timescale)
    .sort((a, b) => new Date(b.publishAt).getTime() - new Date(a.publishAt).getTime())
    .map(normalizeEntryCopy);
}

export function getTodayLogs(): LogEntry[] {
  const now = new Date();
  const today = now.toISOString().split('T')[0];
  return LOG_ENTRIES
    .filter(entry => {
      const entryDate = new Date(entry.publishAt);
      const entryDay = entryDate.toISOString().split('T')[0];
      return entry.timescale === '1d' && entryDay <= today;
    })
    .sort((a, b) => new Date(a.publishAt).getTime() - new Date(b.publishAt).getTime())
    .map(normalizeEntryCopy);
}
